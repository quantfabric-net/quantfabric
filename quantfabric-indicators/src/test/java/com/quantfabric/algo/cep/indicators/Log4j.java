package com.quantfabric.algo.cep.indicators;

import org.apache.log4j.ConsoleAppender;
import org.apache.log4j.Level;
import org.apache.log4j.Logger;
import org.apache.log4j.PatternLayout;

public class Log4j
{
	public static void init()
	{
		Logger.getRootLogger().addAppender(new ConsoleAppender(new PatternLayout("%d{ABSOLUTE} [%t] %-5p %c{1} - %m%n"), ConsoleAppender.SYSTEM_OUT));
		Logger.getLogger("com.espertech.esper").setLevel(Level.INFO);
	}
}
